    public void nguyenWidrow(){
        //faktor skala
        double input = (double)1/6;
        double hiddenLayer = 3;
        double faktorSkala = Double.valueOf(df4.format(0.7*(Math.pow(hiddenLayer, input))));
        double absV1 = 0;
        double absV2 = 0;
        double absV3 = 0;
        double absW = 0;
        df2 = new DecimalFormat("#.##");
        v_weighted_after_nguyen = v_weighted;
        w_weighted_after_nguyen = w_weighted;
        
        for (double[] result : v_weighted) {
            absV1 += Math.pow(result[0], 2);
            absV2 += Math.pow(result[1], 2);
            absV3 += Math.pow(result[2], 2);
        }
        double absV1Sqrt = Double.valueOf(df2.format(Math.sqrt(absV1)));
        double absV2Sqrt = Double.valueOf(df2.format(Math.sqrt(absV2)));
        double absV3Sqrt = Double.valueOf(df2.format(Math.sqrt(absV3)));
        double absBiasV = 0.3;
        for (int i = 0; i < w_weighted.length; i++) {
            absW += Math.pow(w_weighted[i], 2);
        }
        
        double absWSqrt = Double.valueOf(df2.format(Math.sqrt(absW)));
        double absBiasW = -0.1;
        
        //bobot v(input - hidden) stlah nguyen
        //System.out.println("\n================= After Nyugen Widrow Process ===========");
        //System.out.println("\nWeighted Hidden Layer");
        for (double[] weighted_after_ngunyen : v_weighted_after_nguyen) {
            for (int j = 0; j < 3; j++) {
                if (j == 0) {
                    weighted_after_ngunyen[j] = faktorSkala * weighted_after_ngunyen[j] / absV1Sqrt;
                } else if (j == 1) {
                    weighted_after_ngunyen[j] = faktorSkala * weighted_after_ngunyen[j] / absV2Sqrt;
                } else {
                    weighted_after_ngunyen[j] = faktorSkala * weighted_after_ngunyen[j] / absV3Sqrt;
                }
                //System.out.print(Double.valueOf(df4.format(weighted_after_ngunyen[j])) + "\t");
            }
            //System.out.println();
        }
        
        //System.out.println("Bias Hidden Layer");
        for (int i = 0; i < biasV.length; i++) {
            biasV[i] = faktorSkala*biasV[i]/absBiasV;
            //System.out.print(Double.valueOf(df2.format(biasV[i]))+"\t");
        }
        
        //bobot w(hidden - output) stlah nguyen
        //System.out.println("\n\nWeighted Ouput Layer");
        for (int i = 0; i < w_weighted_after_nguyen.length; i++) {
            w_weighted_after_nguyen[i] = faktorSkala*w_weighted_after_nguyen[i]/absWSqrt;
            //System.out.println(Double.valueOf(df2.format(w_weighted_after_nguyen[i]))+"\t");
        }
        
        //System.out.println("Bias Output Layer");
        biasW = biasW*faktorSkala/absBiasW;
        //System.out.println(biasW);
    }
    
